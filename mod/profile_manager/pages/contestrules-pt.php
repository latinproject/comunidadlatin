<h3>12. ANEXO</h3>

<h4>Produção de Conteúdo</h4>

<p>Etapas sugeridas para a elaboração:</p>

<ol>
<li>Caracterização dos estudantes meta e definição dos objetivos.</li>
<li>Coleta de informação com base em bibliografia atualizada.</li>
<li>Elaboração de conteúdos teóricos e práticos.</li>
<li>Elaboração de atividades, avaliações, autoavaliações e demais recursos educativos a serem incorporados.</li>
<li>Elaborar bibliografia atualizada, índices, guias de uso.</li>
<li>Realizar correções de conteúdo e ortografia.</li>
<li>Realizar a diagramação e o design gráfico, caso necessário.</li>
<li>Teste, correção e versão final.</li>

<p>Cada uma destas etapas deverá contar com a participação colaborativa dos participantes do grupo.</p>
<p>Esta tarefa exige a definição de acordos, papéis e tomada de decisões, assim como a uma estratégia por parte do grupo docente envolvido. Sugerimos algumas possíveis formas de organização dos processos de escrita colaborativa:</p>

<ol>
<li>O grupo planeja e define a tarefa, logo cada participante prepara sua parte e o grupo compila as partes individuais e revisa o documento.</li>
<li>O grupo planeja e define a tarefa, logo um integrante do grupo prepara um rascunho, a equipe edita e revisa o rascunho.</li>
<li>Um integrante do grupo planeja e escreve um rascunho, o grupo revisa o rascunho.</li>
<li>Uma pessoa planeja e escreve um rascunho, depois um ou mais integrantes da equipe revisa o rascunho sem consultar o autor original.</li>
<li>O grupo planeja e escreve o rascunho, um ou mais integrantes revisam o rascunho sem consultar o autor original.</li>
<li>Uma pessoa delega as tarefas, cada integrante completa seu trabalho individual, uma pessoa revisa o documento.</li>

<p>Também é possível que o grupo integre todas ou qualquer dos seguintes papéis:</p>

<li>Criador de conteúdo: este papel representa uma pessoa ou um grupo de pessoas que utilizam diversas metodologias extraídas da escrita colaborativa para criar conteúdo sobre um tema especifico.</li>
<li>Gerador de ideias: esta pessoa (ou grupo) identifica o panorama geral do projeto, levando os outros em direção ao objetivo principal, e mantém o fluxo do projeto.</li>
<li>Colaborador avaliador de conteúdo: esta função está a cargo da revisão dos conteúdos. Esta pessoa (ou grupo) deve trabalhar em estreita relação com o Organizador de conteúdo, propondo as modificações apropriadas.</li>
<li>Revisor técnico: esta pessoa (ou grupo) tem a função de controlar os aspectos técnicos do livro e do conteúdo, por exemplo, verificar a consistência das conexões, revisão a edição de fórmulas etc.</li>
<li>Colaborador pedagógico: esta pessoa assegura que a organização do conteúdo faz sentido do ponto de vista pedagógico. Para tanto, ele/ela trabalha em estreita colaboração com o organizador de conteúdo com o fim de notificar o conteúdo que falta, e para sanar alguns dos elementos que devem ser mais aprofundados ou incorporados.</li>
<li>Avaliador de idioma: um falante nativo tem que revisar o livro com o fim de assegurar  se sua ortografia, sintaxe e a gramática seguem as normas do idioma.</li>
<li>Organizador de conteúdo: uma pessoa capaz de guiar a criação de um livro, que tem ideias claras do plano de estudos e das necessidades dos estudantes, e que proporciona rápida revisão e comentários sobre todos os aspectos do conteúdo. Além disso, ele/ela propõe a tabela de conteúdos e é responsável por definir a estratégia de escrita colaborativa no grupo e a alocação de tarefas na plataforma.</li>
<li>Organizador: esta função é vital para organizar a equipe que trabalha no projeto. A pessoa em cargo dessa função deve possuir diversas competências com a gestão de projetos, boas relações sociais, experiência no âmbito da colaboração, e conhecimentos básicos sobre o tema do livro.</li>
<li>Designer de interface: esse designer de desenvolve e mantém o modelo de cada livro para assegurar-se que o conteúdo produzido pelos diferentes colaboradores (Produtores de conteúdos) coincida com o mesmo look-and-feel. Essa pessoa é responsável pelo design e implementação e melhorias necessárias em um curto período de tempo.</li>
<li>Designer do modelo pedagógico: oferece uma maneira uniforme para preencher a tabela de conteúdos de conteúdos estabelecidos pelo organizador de conteúdo, e assegura que o conteúdo dos diversos capítulos a serem organizados segue uma estrutura comum e seguem uma estratégia pedagógica. </li>
<li>Tradutor: quando o idioma nativo da produção de conteúdo não coincide com a língua do público-alvo, o tradutor garante a tradução dos conteúdos para a língua de destino. Em seguida, a tradução tem que ser validade por um revisor de idioma. Esse papel é crucial para o contexto latino-americano, em relação a seus principais idiomas: espanhol (em torno de 360 milhões de falantes nativos na região) e português (em torno de 200 milhões de falantes nativos só no Brasil).</li>
