<h3>8. AVALIDAÇÃO DAS PROPOSTAS</h3>

<p>As soluções apresentadas serão estudadas  por um Comitê Conselheiro, formado por acadêmicos das universidades parceiras do consorcio, designadas pelo LATIn com o objetivo de avaliar as propostas apresentadas e elaborar uma lista das mesmas. Para cada uma delas o Comitê realizará uma fundamentação que justifique sua inclusão ou não na lista de propostas a serem apoiadas.</p>
<p>Para a avaliação das propostas será considerado:</p>

<ol>
<li>A relevância, atualidade e inovação do tema proposto.</li>
<li>A validade e/ou atualidade dos conteúdos</li>
<li>A contribuição do material para a comunidade educativa disciplinar.</li>
<li>A falta de bibliografia disponível no meio.</li>
<li>A relevância da proposta de acordo com os planos de estudo das instituições sociais.</li>
<li>A coerência entre os conteúdos que serão abordados e os objetivos que se planejam cumprir.</li>
<li>A adequação da proposta aos destinatários definidos.</li>
<li>A relevância e atualidade da bibliografia de base.</li>
<li>A adequação dos recursos educativos e atividades que se prevê incluir com o propósito de reforçar e/ou complementar o conteúdo teórico e prático.</li>
<li>A adequação dos mecanismos de colaboração para a escrita do livro didático aberto esteja apresentadas na proposta.</li>
<li>A adequação do cronograma previsto aos prazos estabelecidos na convocatória.</li>
</ol>

<h3>9. IMPLEMENTAÇÃO DAS PROPOSTAS</h3>

<p>Depois de serem selecionadas e comunicadas as propostas ganhadoras, os grupos começaram a desenvolver o processo de produção de conteúdo de livros didáticos abertos.</p>
<p>O conteúdo elaborado será revisado pelo Comitê Conselheiro que credenciará e validará cada livro elaborado seguindo critérios de qualidade e comprimento da proposta prevista.</p>
<p>Será realizada a validação da qualidade dos livros de acordo com aspectos de estilo, conteúdo, técnicos, pedagógicos e linguísticos. A credenciação consiste em um processo de revisão para garantir que os livros satisfazem as necessidades dos estudantes da América-latina. </p>
<p>O grupo referente receberá uma cópia dos relatórios de avaliação. Se forem favoráveis, os autores incorporarão em seu trabalho as modificações sugeridas pelo Comitê Conselheiro em um prazo de do máximo 30 dias. Ao finalizar, entregará a obra corrigida, com as modificações solicitadas.</p>
<p>O Comitê Editorial do LATIn verificará as obras feitas pelo autor e ver se a mesma é um plágio, caso sim, a obra será rejeitada. Além disso, o Comitê verificará se o texto está de acordo com o Manual de Estilo do LATIn, que será publicado na web do projeto. Os critérios de avaliação serão: texto claro e coerente, referência a autores citados adequada, bibliografia completa, elementos gráficos e multimídia para receber tratamento editorial. O Comitê Editorial realizará a correção ortográfica e de estilo da obra e, se necessário, se baseará em experts na área.</p>
<p>Os livros serão entregues na plataforma criada para este fim pelo projeto LATIn.</p>
<p>Uma vez que credenciação tenha se estabelecido, os livros serão publicados e distribuídos através da Plataforma LATIn.</p>
<p>Todos os livros realizados e editados pela plataforma LATIn serão gratuitos, os livros físicos (impressos) serão distribuídos a preço de custo. Este preço inclui unicamente aqueles custos relacionados com sua impressão (papel, tinta e gastos administrativos) e os custos de distribuição. O preço resultante não determina o "uso comercial" das obras pois não existem finalidades de lucro.</p>


